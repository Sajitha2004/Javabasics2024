package org.Condition;


public class Condition {


	       private void main (int a)  {
	    	   if(a%2 == 0) {
	    		   System.out.println("Even");
	    	   }
	    	   else {
	    		   System.out.println("odd");
	    	   }
	       }
	       public static void main(String[] args) {
			 Condition c = new Condition();
			 c.main(1);
		}

}